import UIKit

extension UIColor {
    
    public class var bgBlue: UIColor {
        return UIColor(displayP3Red: 67/255, green: 78/255, blue: 232/255, alpha: 1.0)
    }
    
    public class var coronaAmber: UIColor {
        return UIColor(displayP3Red: 253/255, green: 249/255, blue: 132/255, alpha: 1.0)
    }
    
    public class var coronaGreen: UIColor {
        return UIColor(displayP3Red: 145/255, green: 242/255, blue: 180/255, alpha: 1.0)
    }
    
    public class var coronaPink: UIColor {
        return UIColor(displayP3Red: 239/255, green: 135/255, blue: 222/255, alpha: 1.0)
    }
    
}
